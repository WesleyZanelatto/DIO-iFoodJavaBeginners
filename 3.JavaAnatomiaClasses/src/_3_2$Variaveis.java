public class _3_2$Variaveis {
    public static void main(String[] args) {
        
        /*Caso seja atribuido o nome "final" na frente do tipo da variável, significa que essa variável 
        é uma constante, ou seja, jamais poderá ser alterada. Ex: final String meuNome = "Wesley"*/

        //Tipo | nome | seu valor
        String meuNome = "Wesley";
        int anoFabricacao = 2022;
        boolean verdadeira = true;
        boolean falso = false;

        //Alterando valores de variáveis
        anoFabricacao = 2018;
        
        //Imprimindo as variáveis na tela
        System.out.println(meuNome + " " + anoFabricacao + " " + verdadeira + " " + falso);

    }

}
